public  class primitivetypes{
    public static void main(String[]args)
    {
        System.out.println("Primitive datatypes are:");
        byte a=120;//-128 to 127
        short b=3_0_0_0_0; //-32,768 to 32767
        int c=12903;//-2^31 to 2^31-1
        long d=123456789L;//very large
        float e=1.123456789F;//6 - 7 digits
        double f=1.2345678902345;//15- 16 digits
        char g='a';//0 to 65535
        char h=65;// which works internally with int
        boolean i=true;
        boolean j=false;
        System.out.println("Integer");
        System.out.println("byte:"+a);
        System.out.println("short:"+b);
        System.out.println("integer:"+c);
        System.out.println("long:"+d);
        System.out.println("Decimal");
        System.out.println("float:"+e);
        System.out.println("Double:"+f);
        System.out.println("Character");
        System.out.println("char:"+g);
        System.out.println("char:"+h);
        System.out.println("Boolean");
        System.out.println("boolean:"+i);
        System.out.println("boolean:"+j);
//these are all the primitivetypes in java programming 
    }
}
