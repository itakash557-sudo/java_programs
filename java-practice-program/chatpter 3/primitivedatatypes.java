public class primitivedatatypes {
    public static void main(String[]args)
    {
        System.out.println("Integer Primitive datatypes in java");
        byte a=120;
        System.out.println("byte value "+a);
        short b=30000;
        System.out.println("short "+b);
        int c=2000000;
        System.out.println("integer "+c);
        long d=1234567890L;
        System.out.println("long "+d);
        System.out.println("Decimal point primitive types in java");
        float e=10.5f;
        System.out.println("float "+e);
        double f=456789.07465367266;
        System.out.println("double "+f);
        System.out.println("Character primitive datatype in java");
        char g='a';
        char g1=65;
        char g2='\u0041';
        char g3='\n';
        System.out.println("Character "+g3);
        System.out.println("Character "+g2);
        System.out.println("Character "+g1);
        System.out.println("Character "+g);
        boolean h=true;
        System.out.println("boolean "+h);
        }
//primitive datatypes in java
}
